    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //BotÃ³n de buscar productos por tienda
      
        jTable1.setVisible(true);
        String tienda=jComboBox1.getSelectedItem().toString();
        String sentencia="match(p:producto)-[r:DISPONIBLE]->(t:tienda) where t.nombre='"+tienda+"' return p.descripcion,r.precio,r.stock";
        System.out.println(sentencia);
        List<Record> datos=miNeo.obtenerDatos(sentencia);
        Object [][]filas=new Object[datos.size()][3];
        int i=0;
        for (Record dato : datos) {
            System.out.println(dato.get(0).toString()+" "+dato.get(1).toString()+" "+dato.get(2).toString());
            filas[i][0]=dato.get(0).toString();
            filas[i][1]=dato.get(1).toString();
            filas[i][2]=dato.get(2).toString();
            i++;
        }
        Object []titulos={"Producto","Precio","Stock"};
               
        DefaultTableModel tm=new DefaultTableModel(filas,titulos);
        jTable1.setModel(tm);
        
    }//GEN-LAST:event_jButton1ActionPerformed

